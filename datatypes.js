/**
 *  There are string, number, null, undefined, boolean, bigint,symbol
 */

// string
let str = "Hello";
console.log(typeof str);  // op will be string

// number
let num = 10;
console.log(typeof num); // op will be  number

// boolean
let isTrue = true;
console.log(typeof isTrue); // op will be boolean

// undefined
let isUndefined;
console.log(typeof isUndefined); // op will be undefined

// null describes that variable does not hold any value
let isNull = null;
console.log(isNull);

// bigInt
let isBigInt = 1n;
console.log(typeof isBigInt); // op will be bigint